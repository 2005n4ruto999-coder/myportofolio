const ARABIC_LABEL = "\u0627\u0644\u0639\u0631\u0628\u064a\u0629";

function getTranslation(key) {
  return translations[currentLanguage][key] ?? translations.en[key] ?? "";
}

function isMobileViewport() {
  return window.innerWidth <= 860;
}

function setMenuState(isOpen) {
  topbarInner.classList.toggle("menu-open", isOpen);
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  menuToggle.setAttribute(
    "aria-label",
    getTranslation(isOpen ? "aria.menuClose" : "aria.menuOpen")
  );
}

function syncModalLock() {
  root.classList.toggle(
    "modal-open",
    videoModal.classList.contains("open") || imageModal.classList.contains("open")
  );
}

function syncLanguageControls() {
  const nextLanguageLabel = currentLanguage === "ar" ? ENGLISH_LABEL : ARABIC_LABEL;
  const ariaLabel = getTranslation(
    currentLanguage === "ar" ? "aria.switchToEnglish" : "aria.switchToArabic"
  );

  translateButton.querySelector("span").textContent = nextLanguageLabel;
  translateButton.setAttribute("aria-label", ariaLabel);
  translateButton.title = ariaLabel;
  videoModalFrame.title = getTranslation("modal.playerTitle");
  return;
  translateButton.querySelector("span").textContent =
    currentLanguage === "ar" ? ENGLISH_LABEL : ARABIC_LABEL;
  translateButton.setAttribute(
    "aria-label",
    getTranslation(currentLanguage === "ar" ? "aria.switchToEnglish" : "aria.switchToArabic")
  );
  videoModalFrame.title = getTranslation("modal.playerTitle");
  return;
  translateButton.querySelector("span").textContent =
    currentLanguage === "ar" ? "English" : "العربية";
  translateButton.setAttribute(
    "aria-label",
    getTranslation(currentLanguage === "ar" ? "aria.switchToEnglish" : "aria.switchToArabic")
  );
  videoModalFrame.title = getTranslation("modal.playerTitle");
}

function syncLanguageControls() {
  const nextLanguageLabel = currentLanguage === "ar" ? ENGLISH_LABEL : ARABIC_LABEL;
  const ariaLabel = getTranslation(
    currentLanguage === "ar" ? "aria.switchToEnglish" : "aria.switchToArabic"
  );

  translateButtonLabel.textContent = nextLanguageLabel;
  translateButton.setAttribute("aria-label", ariaLabel);
  translateButton.title = ariaLabel;
  videoModalFrame.title = getTranslation("modal.playerTitle");
}

function applyTranslations(language) {
  currentLanguage = language === "ar" ? "ar" : "en";
  html.lang = currentLanguage;
  html.dir = currentLanguage === "ar" ? "rtl" : "ltr";
  document.title = getTranslation("pageTitle");

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const value = getTranslation(key);
    if (value) {
      node.textContent = value;
    }
  });

  translatableAriaNodes.forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    const value = getTranslation(key);
    if (value) {
      node.setAttribute("aria-label", value);
    }
  });

  syncLanguageControls();
  setMenuState(topbarInner.classList.contains("menu-open"));

  if (videoModal.classList.contains("open")) {
    videoModalTitle.textContent =
      currentLanguage === "ar"
        ? videoModal.dataset.titleAr || videoModal.dataset.titleEn || getTranslation("modal.videoTitle")
        : videoModal.dataset.titleEn || videoModal.dataset.titleAr || getTranslation("modal.videoTitle");
  }

  if (imageModal.classList.contains("open")) {
    imageModalImage.alt =
      currentLanguage === "ar"
        ? imageModal.dataset.altAr || imageModal.dataset.altEn || getTranslation("frames.imageAltFallback")
        : imageModal.dataset.altEn || imageModal.dataset.altAr || getTranslation("frames.imageAltFallback");
  }

}

function openVideoModal(videoId, titleEn, titleAr) {
  videoModal.dataset.titleEn = titleEn || getTranslation("modal.videoTitle");
  videoModal.dataset.titleAr = titleAr || titleEn || getTranslation("modal.videoTitle");
  videoModalFrame.src = `https://www.youtube-nocookie.com/embed/${videoId}?autoplay=1&rel=0`;
  videoModalTitle.textContent =
    currentLanguage === "ar" ? videoModal.dataset.titleAr : videoModal.dataset.titleEn;
  videoModal.classList.add("open");
  videoModal.setAttribute("aria-hidden", "false");
  syncModalLock();
}

function closeVideoModalDialog() {
  videoModal.classList.remove("open");
  videoModal.setAttribute("aria-hidden", "true");
  videoModalFrame.src = "about:blank";
  syncModalLock();
}

function openImageModal(src, altEn, altAr) {
  imageModal.dataset.altEn = altEn || getTranslation("frames.imageAltFallback");
  imageModal.dataset.altAr = altAr || altEn || getTranslation("frames.imageAltFallback");
  imageModalImage.src = src;
  imageModalImage.alt =
    currentLanguage === "ar" ? imageModal.dataset.altAr : imageModal.dataset.altEn;
  imageModal.classList.add("open");
  imageModal.setAttribute("aria-hidden", "false");
  syncModalLock();
}

function closeImageModalDialog() {
  imageModal.classList.remove("open");
  imageModal.setAttribute("aria-hidden", "true");
  imageModalImage.src = "";
  imageModalImage.alt = getTranslation("frames.imageAltFallback");
  syncModalLock();
}

menuToggle.addEventListener("click", (event) => {
  event.stopPropagation();
  setMenuState(!topbarInner.classList.contains("menu-open"));
});

translateButton.addEventListener("click", () => {
  applyTranslations(currentLanguage === "ar" ? "en" : "ar");
});

videoTriggers.forEach((trigger) => {
  trigger.addEventListener("click", () => {
    openVideoModal(
      trigger.dataset.videoId,
      trigger.dataset.videoTitleEn || trigger.dataset.videoTitle || getTranslation("modal.videoTitle"),
      trigger.dataset.videoTitleAr || trigger.dataset.videoTitleEn || getTranslation("modal.videoTitle")
    );
  });
});

zoomablePhotos.forEach((photo) => {
  photo.addEventListener("click", () => {
    const labelKey = photo.dataset.i18nAriaLabel;
    openImageModal(
      photo.dataset.zoomSrc,
      translations.en[labelKey] || photo.getAttribute("aria-label") || translations.en["frames.imageAltFallback"],
      translations.ar[labelKey] || photo.getAttribute("aria-label") || translations.ar["frames.imageAltFallback"]
    );
  });
});

closeVideoModal.addEventListener("click", closeVideoModalDialog);
videoModal.addEventListener("click", (event) => {
  if (event.target === videoModal) {
    closeVideoModalDialog();
  }
});

closeImageModal.addEventListener("click", closeImageModalDialog);
imageModal.addEventListener("click", (event) => {
  if (event.target === imageModal) {
    closeImageModalDialog();
  }
});

document.querySelectorAll(".topbar-menu a").forEach((link) => {
  link.addEventListener("click", () => {
    if (isMobileViewport()) {
      setMenuState(false);
    }
  });
});

document.addEventListener("click", (event) => {
  if (!isMobileViewport() || !topbarInner.classList.contains("menu-open")) {
    return;
  }

  if (!topbar.contains(event.target)) {
    setMenuState(false);
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key !== "Escape") {
    return;
  }

  if (imageModal.classList.contains("open")) {
    closeImageModalDialog();
  }

  if (videoModal.classList.contains("open")) {
    closeVideoModalDialog();
  }

  if (topbarInner.classList.contains("menu-open")) {
    setMenuState(false);
  }
});

window.addEventListener("resize", () => {
  if (!isMobileViewport()) {
    setMenuState(false);
  }
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

revealItems.forEach((item) => revealObserver.observe(item));

const navObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) {
        return;
      }

      navLinks.forEach((link) => {
        link.classList.toggle("active", link.getAttribute("href") === `#${entry.target.id}`);
      });
    });
  },
  {
    rootMargin: "-35% 0px -55% 0px",
    threshold: 0,
  }
);

observedSections.forEach((section) => navObserver.observe(section));

applyTranslations("en");
