const html = document.documentElement;
const shortFormGrid = document.getElementById("short-form-grid");
const longFormGrid = document.getElementById("long-form-grid");
const translateButton = document.getElementById("translate-button");
const translateButtonLabel = translateButton.querySelector(".translate-button-label");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const translatableAriaNodes = document.querySelectorAll("[data-i18n-aria-label]");
const translatableAltNodes = document.querySelectorAll("[data-i18n-alt]");
const translatableTitleNodes = document.querySelectorAll("[data-i18n-title]");
const videoModal = document.getElementById("video-modal");
const videoModalDialog = videoModal.querySelector(".video-modal-dialog");
const videoModalFrame = document.getElementById("video-modal-frame");
const videoModalTitle = document.getElementById("video-modal-title");
const closeVideoModal = document.getElementById("close-video-modal");

let currentLanguage = "en";
let currentPreviewVideoId = "";

const ENGLISH_LABEL = "English";
const ARABIC_LABEL = "العربية";

function getTranslation(key) {
  return translations[currentLanguage][key] ?? translations.en[key] ?? "";
}

function getLocalized(value) {
  if (typeof value === "string") {
    return value;
  }

  return value?.[currentLanguage] ?? value?.en ?? "";
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function syncLanguageControls() {
  const nextLanguageLabel = currentLanguage === "ar" ? ENGLISH_LABEL : ARABIC_LABEL;
  const ariaLabel = getTranslation(
    currentLanguage === "ar" ? "aria.switchToEnglish" : "aria.switchToArabic"
  );

  translateButtonLabel.textContent = nextLanguageLabel;
  translateButton.setAttribute("aria-label", ariaLabel);
  translateButton.title = ariaLabel;
  videoModalFrame.title = getTranslation("modal.playerTitle");
}

function applyTranslations(language) {
  currentLanguage = language === "ar" ? "ar" : "en";
  html.lang = currentLanguage;
  html.dir = currentLanguage === "ar" ? "rtl" : "ltr";
  document.title = getTranslation("pageTitle");

  translatableNodes.forEach((node) => {
    const value = getTranslation(node.dataset.i18n);
    if (value) {
      node.textContent = value;
    }
  });

  translatableAriaNodes.forEach((node) => {
    const value = getTranslation(node.dataset.i18nAriaLabel);
    if (value) {
      node.setAttribute("aria-label", value);
    }
  });

  translatableAltNodes.forEach((node) => {
    const value = getTranslation(node.dataset.i18nAlt);
    if (value) {
      node.setAttribute("alt", value);
    }
  });

  translatableTitleNodes.forEach((node) => {
    const value = getTranslation(node.dataset.i18nTitle);
    if (value) {
      node.setAttribute("title", value);
    }
  });

  syncLanguageControls();
  renderLibrary();

  if (currentPreviewVideoId) {
    const selectedVideo = videoLibrary.find((video) => video.id === currentPreviewVideoId);
    videoModalTitle.textContent = selectedVideo
      ? getLocalized(selectedVideo.title)
      : getTranslation("modal.videoTitle");
  }
}

function getPlatformLabel(video) {
  return video.platform;
}

function getFormatLabel(video) {
  return getTranslation(video.format === "short" ? "format.short" : "format.long");
}

function getPrimaryActionLabel(video) {
  return video.videoId || video.embedUrl
    ? getTranslation("actions.previewHere")
    : getTranslation("actions.viewProject");
}

function getSecondaryActionLabel(video) {
  const platformKey = {
    Instagram: "actions.openInstagram",
    LinkedIn: "actions.openLinkedin",
    YouTube: "actions.openYoutube",
  }[video.platform];

  return getTranslation(platformKey ?? "actions.viewProject");
}

function getSampleLabel(video) {
  return currentLanguage === "ar"
    ? `${getTranslation("card.sample")} ${video.platform}`
    : `${video.platform} ${getTranslation("card.sample")}`;
}

function createTagMarkup(tags) {
  return getLocalized(tags)
    .map((tag) => `<span class="tag">${escapeHtml(tag)}</span>`)
    .join("");
}

function createCardMediaMarkup(video, title) {
  const posterMarkup = video.poster
    ? `<img src="${escapeHtml(encodeURI(video.poster))}" alt="${escapeHtml(`${title} ${getTranslation("card.previewAlt")}`)}" loading="lazy" onerror="this.remove()" />`
    : "";

  return `
    ${posterMarkup}
    <div class="card-media-fallback">
      <span>${escapeHtml(video.platform === "Instagram" ? getTranslation("card.instagramFallback") : video.platform)}</span>
    </div>
  `;
}

function createCardMarkup(video, index) {
  const hasPreview = Boolean(video.videoId || video.embedUrl);
  const title = getLocalized(video.title);
  const primaryAction = hasPreview
    ? `<button class="card-button card-button--primary" type="button" data-preview-id="${escapeHtml(video.id)}">${escapeHtml(getPrimaryActionLabel(video))}</button>`
    : `<a class="card-button card-button--primary" href="${escapeHtml(video.href)}" target="_blank" rel="noreferrer">${escapeHtml(getPrimaryActionLabel(video))}</a>`;
  const secondaryAction = hasPreview
    ? `
          <a class="card-button card-button--secondary" href="${escapeHtml(video.href)}" target="_blank" rel="noreferrer">
            ${escapeHtml(getSecondaryActionLabel(video))}
          </a>
        `
    : "";

  const previewPill = hasPreview
    ? `<div class="preview-pill">${escapeHtml(getTranslation("card.previewAvailable"))}</div>`
    : `<div class="preview-pill">${escapeHtml(getTranslation("card.externalPost"))}</div>`;

  return `
    <article class="gallery-card" style="--card-index: ${index}" data-format="${escapeHtml(video.format)}">
      <div class="card-media card-media--${escapeHtml(video.format)}">
        ${createCardMediaMarkup(video, title)}
        <div class="card-badges">
          <span class="badge">${escapeHtml(getFormatLabel(video))}</span>
          <span class="badge">${escapeHtml(getPlatformLabel(video))}</span>
        </div>
        ${previewPill}
      </div>

      <div class="card-body">
        <div class="card-meta">
          <span>${escapeHtml(getSampleLabel(video))}</span>
        </div>
        <h3 class="card-title">${escapeHtml(title)}</h3>
        <p class="card-copy">${escapeHtml(getLocalized(video.description))}</p>
        <div class="tag-list">${createTagMarkup(video.tags)}</div>
        <div class="card-actions">
          ${primaryAction}
          ${secondaryAction}
        </div>
      </div>
    </article>
  `;
}

function renderLibrary() {
  const shortVideos = videoLibrary.filter((video) => video.format === "short");
  const longVideos = videoLibrary.filter((video) => video.format === "long");

  shortFormGrid.innerHTML = shortVideos
    .map((video, index) => createCardMarkup(video, index))
    .join("");

  longFormGrid.innerHTML = longVideos
    .map((video, index) => createCardMarkup(video, index + shortVideos.length))
    .join("");
}

function getEmbedUrl(video) {
  if (video.embedUrl) {
    return video.embedUrl;
  }

  return `https://www.youtube-nocookie.com/embed/${video.videoId}?autoplay=1&rel=0`;
}

function openVideoModal(video) {
  if (!video || (!video.videoId && !video.embedUrl)) {
    return;
  }

  currentPreviewVideoId = video.id;
  videoModalTitle.textContent = getLocalized(video.title);
  videoModalFrame.src = getEmbedUrl(video);
  videoModalDialog.classList.toggle("video-modal-dialog--vertical", video.format === "short");
  videoModal.classList.add("open");
  videoModal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
}

function closeVideoModalDialog() {
  currentPreviewVideoId = "";
  videoModal.classList.remove("open");
  videoModal.setAttribute("aria-hidden", "true");
  videoModalFrame.src = "about:blank";
  videoModalDialog.classList.remove("video-modal-dialog--vertical");
  document.body.classList.remove("modal-open");
}

translateButton.addEventListener("click", () => {
  applyTranslations(currentLanguage === "ar" ? "en" : "ar");
});

document.addEventListener("click", (event) => {
  const previewTrigger = event.target.closest("[data-preview-id]");

  if (previewTrigger) {
    const selectedVideo = videoLibrary.find((video) => video.id === previewTrigger.dataset.previewId);
    openVideoModal(selectedVideo);
    return;
  }

  if (event.target === videoModal) {
    closeVideoModalDialog();
  }
});

closeVideoModal.addEventListener("click", closeVideoModalDialog);

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && videoModal.classList.contains("open")) {
    closeVideoModalDialog();
  }
});

